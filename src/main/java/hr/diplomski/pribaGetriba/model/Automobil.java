package hr.diplomski.pribaGetriba.model;

public class Automobil {
	private int id;
	private String marka;
	private String model;
	private String tip;
	private String vin;
	private String registracija;
	private String mjenjac;
	private String gorivo;
	private String kilometri;
	private String snaga;
	private String godinaProizvodnje;
	

	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMarka() {
		return marka;
	}
	public void setMarka(String marka) {
		this.marka = marka;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}
	public String getRegistracija() {
		return registracija;
	}
	public void setRegistracija(String registracija) {
		this.registracija = registracija;
	}
	public String getMjenjac() {
		return mjenjac;
	}
	public void setMjenjac(String mjenjac) {
		this.mjenjac = mjenjac;
	}
	public String getGorivo() {
		return gorivo;
	}
	public void setGorivo(String gorivo) {
		this.gorivo = gorivo;
	}
	public String getKilometri() {
		return kilometri;
	}
	public void setKilometri(String kilometri) {
		this.kilometri = kilometri;
	}
	public String getSnaga() {
		return snaga;
	}
	public void setSnaga(String snaga) {
		this.snaga = snaga;
	}
	public String getGodinaProizvodnje() {
		return godinaProizvodnje;
	}
	public void setGodinaProizvodnje(String godinaProizvodnje) {
		this.godinaProizvodnje = godinaProizvodnje;
	}
	
	
}
