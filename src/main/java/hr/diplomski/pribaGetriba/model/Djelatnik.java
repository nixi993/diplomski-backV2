package hr.diplomski.pribaGetriba.model;



public class Djelatnik {
	
	private int id;
	private String ime;
	private String prezime;
	private String telefon;
	private String email;
	private String username;
	private String password;
	private String vrstaDjelatnika;
	
	
	
	
	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}

	public String getIme() {
		return ime;
	}

	public void setIme(String ime) {
		this.ime = ime;
	}
	public String getPrezime() {
		return prezime;
	}
	public void setPrezime(String prezime) {
		this.prezime = prezime;
	}
	public String getTelefon() {
		return telefon;
	}
	public void setTelefon(String telefon) {
		this.telefon = telefon;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getVrstaDjelatnika() {
		return vrstaDjelatnika;
	}
	public void setVrstaDjelatnika(String vrstaDjelatnika) {
		this.vrstaDjelatnika = vrstaDjelatnika;
	}
	
	
}


